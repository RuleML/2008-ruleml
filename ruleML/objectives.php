          <h2>Objectives</h2>

       

          <p>In recent years rule based technologies have enjoyed

          remarkable adoption in two areas: (1) Business Rules

          Processing and (2) Web-Centred Reasoning. The first trend is

          caused by the software development life cycle which needs to be

          accelerated at reduced cost. The second trend is related to the

          Semantic Web and Service-oriented technologies which aim to

          turn the Web into a huge repository of cross-referenced,

          machine-understandable data and processes. For both trends,

          rules can be used to extract, derive, transform, and integrate

          information in a platform-independent manner. While early

          rule engines and environments were complex, expensive to

          maintain, and not very user friendly, the current generation

          of rule technology provides enhanced usability, scalability and

          performance, and is less costly. A general advantage of

          using rules is that they are usually represented in a

          platform independent manner, often using XML. This fits well

          into today's distributed, heterogeneous Web-based system

          environments. Rules represented in standardized Web formats

          can be discovered, interchanged and invoked at runtime

          within and across Web systems, and can be interpreted and

          executed on any platform.</p>



          <p>Collocated with the 11th International Business Rules

          Forum, the 2008 International Symposium on Rule Interchange

          and Applications (RuleML-2008) is the second symposium

          (after last year's highly successful <a

          href="2007.ruleml.org">RuleML-2007</a>) devoted to work on

          practical distributed rule technologies and rule-based

          applications which need language standards for rules

          operating in the context of, e.g., the Semantic Web,

          Intelligent Multi-Agent Systems, Event-Driven Architectures

          and Service-Oriented Computing Applications. The RuleML

          symposium is a new kind of event where the Web Rules and Logic

          community joins the established, practically oriented <a

          href="http://www.businessrulesforum.com">Forum of the

          Business Rules community</a> to help cross-fertilizing

          between Web and Business Logic technology. The goal of

          RuleML-2008 is to bring together rule system providers,

          representatives of, and participants in, rule

          standardization efforts (e.g., SBVR, RuleML, RIF, PRR, CL)

          and open source rules communities (e.g., jBoss Rules,

          CLIPS/Jess, Prova, OO jDrew, Mandarax, XSB, XQuery),

          practitioners and technical experts, developers, users, and

          researchers. They will be offered an exciting venue to

          exchange new ideas, practical developments and experiences

          on issues pertinent to the interchange and application of

          rules in open distributed environments such as the Web.</p>

          

          <p> The

          Symposium gives emphasis on practical issues such as

          technical contributions and show case demonstrations of

          effective, practical, deployable rule-based technologies,

          rule interchange formats and applications as well as

          discussions of lessons learned that have to be taken into

          account when employing rule-based technologies in

          distributed, (partially) open, heterogeneous

          environments. We also welcome groundwork that helps to build

          an effective, practical, and deployable rule standard,

          improve rule technology, provide better understanding of the

          integration and interchange of rules, and make the current

          generation of rule engines and rule technology more usable

          for advanced Web and Service Oriented Architectures.</p>



