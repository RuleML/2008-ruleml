          with the 11th Business Rules Forum.</p>

	  <p>RuleML-2008 is the primary industry-oriented event of the
          Web Logic community that again (following last year's <a
          href="http://2007.ruleml.org">RuleML-2007</a>) joins the
          established, practically-oriented <a
          href="http://www.businessrulesforum.com">Forum of the
          Business Rules community</a> to help cross-fertilization of
          ideas between Web and Business Logic technologies.  It is
          devoted to practical distributed rule technologies and
          rule-based applications which need language standards for
          rules operating in the context of, amongst other things, the
          Semantic Web, Intelligent Multi-Agent Systems, Event-Driven
          Architectures and Service-Oriented Computing
          Applications.</p>

	  <p>RuleML-2008 provides an excellent opportunity for your
