        <p>&nbsp;</p>
        <p>2008 sees a major step forward for rules on the Web. The <a href="http://www.w3.org/2005/rules/wiki/RIF_Working_Group">W3C Rule  Interchange Format (RIF) Working Group</a>   will publish several public  working drafts. The OMG has published its <a href="http://www.omg.org/cgi-bin/doc?dtc/07-11-04.pdf">beta specification for Production  Rules Representation</a>, which is being  aligned with W3C's RIF, and will publish the first revision of Semantics of  Business Vocabulary and Business Rules (SBVR) in October. RuleML has published  drafts on Reaction Rules. People around the world are working on  implementations of these specifications. In parallel, business rules are  already playing an important role in Web Services. How will new standards and  current practice align?</p>
        <p>If you are interested in rules on the Web, pick up a boxed lunch and  come to this joint BR Forum and RuleML panel session in the BRF keynote room (Great Hall North).  Our panel will include specifiers and implementers. Our focus will be: when and  how will we use rules on the Web?</p>
