          <h4 style="clear: left;">Abstract</h4>

          <p>The convergence of business rules with business  process management (BPM) has been predicted for many years and is now a matter  of fact. Every major BPM vendor has incorporated or acquired rules technology  within their products and platforms. However, most rules offerings are only  loosely integrated with processes at the task level. The use of business rules  remains largely confined to managing isolated decisions services. Weak  integration and isolation effectively relegates rules to an implementing role  rather than a first class citizen in the capture and management of enterprise  knowledge. </p>
          <p>As the largest vendors bring their rules  offerings to market and as standards from the W3C and OMG mature to adequacy,  the opportunity for vendor-agnostic business rules management systems (BRMS)  approaches. And continued improvement in end-user accessibility of BRMS  promises ever less technical and ever more semantic expression and management  of enterprise knowledge, including process and service models in addition to  data models and business rules. </p>
          <p>The ability to interchange more semantic  models across major vendor offerings promises to dramatically increase the  market demand for reusable, enterprise-relevant knowledge. But as knowledge  becomes increasingly declarative and independent of implementations, it naturally  becomes more ontological. Unfortunately, current ontological technologies are  functionally inadequate from a business process or event processing  perspective. These inadequacies include the lack of ontology for events,  processes, states, actions, and other concepts that relate to change over time.  Without such ontologies, rules or logic that govern processes or react to  events must remain at the level of procedural implementation rather than  declarative knowledge. </p>
          <p>Until BRMS understand rules that refer to  activities and events occurring within business processes, business rules applications  will remain largely confined to discrete decisions, such as encapsulation  within a decision service. By incorporating an adequate ontology of events and  action, however, the knowledge management capabilities first developed in BRMS  will broaden to encompass much of BPM and complex event processing (CEP). Given  the fact that BRMS has been incorporated by the dominant platform vendors,  modeling should move up from the relatively narrow perspective of a BRMS into  the broader context of BPM and CEP. </p>
          <p>The migration of business rules management  into event and process contexts will emphasize the separation of business rules  into ontology versus behavior. Modeling event-driven and business processes  will correspond to defining ontology. Implementing event-driven and business  processes will invoke behaviors at runtime. The ontology will be the same for  BPM, CEP, or the BRMS, as will the behaviors. But the BRMS will know what is  happening in terms of events and processes and it will know what processes it can  invoke and what events it can signal. </p>
          <p>As ontology management becomes increasingly  relevant across application development, the limitations of related standards  will also come into clearer focus. Commercial BRE are, for the most part,  production rule systems that emphasize action over logic. This is best  addressed by OMG's production rule representation (PRR) standard. But PRR is an  isolated standard that includes no support for ontology or logic. W3C's  web-ontology language (OWL) and rule interchange format (RIF) standards address  ontology and logic, but not change or action. The same is true of OMG's SBVR,  which emphasizes linguistics, in addition to ontology and logic, albeit in a  way that remains disconnected with the OMG stack, including PRR. </p>
          <p>Bridging events, processes and other  aspects of reality that occur and change over time and incorporating action is  a fundamental challenge for semantic technologies, especially formal logic,  that must be addressed in a practical manner before rules standards and  semantic technologies will bear substantial fruit in enterprise contexts.</p>
