          <h4 style="clear: left;">Abstract</h4>

          <p>The Rule Interchange Format (RIF) is a W3C activity aimed
          at developing a Web standard for exchanging rules. The need
          for rule-based information processing on the Semantic Web
          has been felt ever since RDF was introduced in the late
          90's. As ontology development picked up pace this decade and
          the limitations of OWL became more apparent, rules were
          firmly put back on the agenda.  RIF is therefore a major
          opportunity for the introduction of rule based technologies
          into the main stream of knowledge representation and
          information processing.</p>

          <p>Despite its humble name, RIF is not just a format and is
          not primarily about syntax. It is an extensible framework
          for rule-based languages, called RIF dialects, which
          includes precise and formal specification of the syntax,
          semantics, and XML serialization of the dialects. In this
          talk we will discuss the main principles behind RIF,
          introduce the RIF extensibility framework, and present the
          Basic Logic Dialect---the only fully developed RIF dialect
          so far. We will also discuss the opportunities for community
          involvement in furthering this standard.</p>
