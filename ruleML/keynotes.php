          <h4 style="clear: left;">Abstract</h4>

          <p>We overview the approach taken by the SILK system, a new, highly ambitious effort to redefine the knowledge representation (KR) playing field for business rules and rule-based process management. The newest part of Vulcan Inc.'s Project Halo, SILK aims to provide key infrastructure for widely-authored VLKBs (Very Large Knowledge Bases) for business and science that answer questions, proactively supply information, and reason powerfully. SILK includes a highly expressive, fully semantic, rule language based expressively on the Hyper Logic Programs KR, together with components for reasoning, web knowledge interchange, and collaborative knowledge acquisition. </p>
          <p>Hyper LP newly synergizes several major strands of pure-research progress in KR based on extensions of declarative logic programs, which are the core KR of RuleML and Rule Interchange Format (RIF) as well as of databases (SQL, XQuery, and SPARQL) and most commercial implementations of OWL ontologies. Hyper LP adds: prioritized defaults cf. courteous and Defeasible Logic; higher-order and frames cf. F-Logic; tight integration of weakened full classical logic (including OWL) cf. generalized Description LP; actions and events cf. production rules, Event-Condition-Action rules, and Situated/Production LP. </p>
          <p>Key challenges for SILK include exploiting natural language in user interaction, parallelism in reasoning, and disjunction in expressiveness. We discuss prospects for the SILK approach to effectively interchange and integrate a high percentage of the world's structured knowledge starting from today's legacy forms. &quot;SILK&quot; stands for &quot;Semantic Inferencing on Large Knowledge&quot;, what the next generation Web will be spun from. </p>
          <p><a href="presentations/keynote4.pdf">Presentation</a></p>
          <h4 style="clear: left;">Biography</h4>
          <p>Benjamin Grosof is a senior research program manager at Vulcan Inc., the company of Paul G. Allen (co-founder of Microsoft).  There he conceived and leads a new large research program in the area of rule-based semantic technologies and artificial intelligence.  Prior to his move to Vulcan in 2007, he was a professor of Information Technology at MIT, in the Sloan School of Management. His research involved the creation of technologies for the new generation web, in which e-services and business communication are more knowledge- and agent-based. In particular, he has pioneered semantic technology and standards for rules, their combination with ontologies, and the Semantic Web. He co-founded the influential RuleML industry standards design effort, and the International Conference on Rules and Rule Markup Languages for the Semantic Web.  He was lead inventor of the rule-based technique which rapidly became the currently dominant approach to commercial implementation of OWL.  </p>
          <p>Prior to joining MIT Sloan in 2000, he was a senior research scientist, in software, at IBM T.J. Watson Research Center (12 years there), where most recently he conceived and led IBM CommonRules and co-led its application piloting for rule-based XML agent contracting in EECOMS, a $29 Million NIST industry-government consortium project on manufacturing supply chain collaboration. His notable technical contributions also include fundamental advances in rule-based intelligent agents, conflict handling for rules, rule-based security authorization, and integration of rules with machine learning. He is author of over 50 refereed publications, three major industry software releases, and two patents. His background includes two years in software startups, a PhD in Computer Science (specialty Artificial Intelligence) from Stanford University, and a BA in Applied Mathematics (specialty Economics and Management Science) from Harvard University.</p>
