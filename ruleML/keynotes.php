          <h4 style="clear: left;">Abstract</h4>

          <p>This talk is about the rise of Complex Event Processing
          (CEP) as we know it today, its historical roots and its
          current position in commercial markets. Some possible
          long-term future roles of CEP in the Information Society
          will be discussed along with the need to develop rule-based
          event hierarchies on a commercial basis to make those
          applications possible. The talk will try to make the point
          that "Rules are everywhere" and that mathematical formalisms
          cannot express all the forms that are in use in various
          event processing systems.</p>

          <h4 style="clear: left;">Biography</h4>

          <p>Professor Luckham's research and consulting activities in
          software technology include multi-processing and business
          processing languages, event-driven systems, complex event
          processing, commercial middleware, program verification,
          systems architecture modelling and simulation, and
          artificial intelligence (automated deduction and reasoning
          systems).</p>

          <p>He has held faculty and invited faculty positions in both
          mathematics and computer science at eight major universities
          in Europe and the United States including Stanford, Harvard
