          <h2>RuleML-2008 Challenge</h2>
       
          <p>The RuleML-2008 Challenge is one of the highlights of
          RuleML-2008. It addresses the system demonstration for
          practical use of rule technologies in distributed and/or
          Web-based environments. The focus of the challenge is on
          rule technologies (including rule languages and engines),
          interoperation and interchange. The challenge offers
          participants the chance to demonstrate their commercial and
          open source tools, use cases, and applications. Prizes will
          be awarded to the two best applications. All accepted demos
          will be presented in a special Challenge Session.</p>

          <p>A submission to RuleML challenge has to meet the
          requirement that declarative rules explicitly play a central
          role in the application. Basically this means that:</p>

	  <ul>

	    <li>Rules are explicitly represented in a declarative
	    format and they are decoupled from the application (rather
	    than being compiled or hard-coded into the application
	    logic).</li>

	    <li>Rules are used in interesting and practically relevant
	    ways to, e.g., derive useful information, transform
	    knowledge, provide decision support and provide automated
	    rule-based monitoring, enforcement, validation or
	    management of the behavioural logic of the
	    application.</li>

	  </ul>	
	  
	  <p>The demo should preferably (but not necessarily) be
	  embedded into a web-based or distributed environment so that
	  there will be a need for features related to the RuleML
	  conference topics, as listed in the call for papers.</p>

