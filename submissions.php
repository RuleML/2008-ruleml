          <h2>Submission Guidelines</h2>

       

          <p>Authors are invited to submit original contributions of

          practical relevance and technical rigor in the field,

          experience reports and show case/use case demonstrations of

          effective, practical, deployable rule-based technologies or

          applications in distributed environments. Papers must be in

          English and should be submitted <a href =

          "http://www.easychair.org/conferences/?conf=ruleml2008">online</a>

          as:</p>



          <ul> <li>Full Papers (15 pages in the proceedings)</li>

          <li>Short Papers (8 pages in the proceedings)</li>

          <li>RuleML-2008 Challenge Demo Paper + Show Cases (up to 8

          pages in the proceedings)</li> </ul>



          <p>Please upload all submissions as PDF files in <a

          href="http://www.springer.de/comp/lncs/authors.html">LNCS

          format</a>. To ensure high quality, submitted papers will be

          carefully peer-reviewed by 3 PC members based on

          originality, significance, technical soundness, and clarity

          of exposition. Authors are requested to upload their

          complete papers by June 2, 2008. The selected papers will be

          published in book form in the Springer Lecture Notes in

          Computer Science (LNCS) series along with a CD with demo software and

          documents. The best paper from all submissions will be determined by

          the PC and a Best Paper Award will be handed over at the Symposium

          by s sponsor. All submissions must be done electronically via <a

          href =

          "http://www.easychair.org/conferences/?conf=ruleml2008">EasyChair</a>.

          We will pursue the publication of a selection of revised

          papers in a special issue of a high-quality journal.</p>



          <h3>RuleML Challenge 2008 Submissions</h3>



          <p>Submissions to the RuleML Challenge 2008 consist of a

          demo paper of up to 8 pages, describing the demo show case,

          and a link to more information about the demo/show case,

          e.g. a project site, an online demonstration, a

          presentation, or a download site. Demo papers should contain

          a substantial presentation of the system to enable a proper

          evaluation of the techniques used.  The content of papers

          should be sufficiently substantial for publication in the

          conference proceedings.  The demo paper should be submitted

          through <a

          href="http://www.easychair.org/conferences/?conf=ruleml2008">EasyChair</a>,

          while the demo link should be submitted through the <a

          href="http://ruleml-challenge.cs.nccu.edu.tw/">Challenge

          Website</a>, after which it will be publicly available

          immediately.</a>



	  </a>If your link is password-protected: then please submit a

	  password for anonymous login from any Web browser, giving us

	  the permission to pass the password on to 3 PC members. The

	  submissions should satisfy the minimal requirements defined

	  in the topics of interest. The demos will be evaluated

	  during RuleML-2008 and prizes will be awarded to the first

	  two best applications.</p>



