    <a name="highlight"><h3>Highlight Talks</h3></a>

    <p>We invite the submission of outstanding full papers that have
    been published between 2007 and the submission deadline (August
    13,2008).  Publications that are "in press" and already linked on
    the journal web site are also welcome. A group of experts will
    select the papers to be presented at the meeting considering the
    impact of the work on the field, the likelihood that the work
    makes a good presentation, and the relevance for the topics of
    RuleML-2008, in general.</p>

    <p>Submissions should be sent directly to the RuleML-2008 chairs
    at ruleml2008@easychair.org by August 13, 2008, and must include
    the following:</p>

    <ul>
      <li>Name/affiliation/email of submitter (assumed to be the
      presenter; note that the presenter cannot change because the
      identity and ability to present of that person will be an
      essential selection criterion).</li>

      <li>Names/affiliations/email of ALL coauthors (note: any name
      appearing on a published paper has to be added here). Note that
      all co-authors have to agree to the submission and that it is
      the responsibility of the submitter to guarantee that all co-
      author email addresses are correct (email notifications of the
      submission will be sent to all co-authors).

      <li>Additional contact information (for presenter).</li>

      <li>A 250-word abstract-like argument that explains how the
      submitted paper(s) suit the goal of presenting highlights that
      impacted the field.</li>

      <li>Sources of original publication(s) (Year, Journal, Vol.,
      pages).</li>

      <li>PDF with paper(s) (note: in case of the submission of 2
      papers, both have to be merged into one single PDF; all reviews
      will be based on the content of this PDF).</li>

      <li>Optional: link to Google Video demonstrating presentation
      skills of presenter.</li>

      <li>Note that we will need PDF submissions; the system will
      neither be able to handle ASCII, nor Word, nor LaTeX, nor
      anything other than standard PDF. It is the responsibility of
      the submitter to verify that the PDF is completely
      viewable/printable by all major operating systems (LINUX, MacOS,
      Windows).</li>

      <li>Each presenter can submit a maximum of one application to
      present a highlight. The maximal number of submissions per
      author/co-author is 5.</li>

    </ul>

    <p>All submissions will be evaluated by a group of reviewers.
    Reviewers will consider the following criteria:</p>

    <ul>

      <li>Relevance, interest, and value of the topic to RuleML-2008
      attendees,</li>

      <li>Impact of the paper(s) on rules (while the impact of papers
      on science is not fully reflected by ISI/Google-like impact
      factors or high number of downloads, high values in such factors
      will clearly stand as a strong argument for acceptance),</li>

      <li>"Presentability" of the work to a large, diverse
      audience,</li>

      <li>Quality of oral presentations by the submitter (if
      none).</li>

    </ul>

    <p>These "soft" criteria attempt to capture the underlying
    concept, namely the presentation of exciting and thought-provoking
    seminars that will both contribute to the success and
    attraction-value of RuleML-2008 and to the impact the meeting has
    on advancing rule interchange and applications. The criterion of
    "presentability" accounts for the fact that some papers that will
    completely change the field, or will become citation records may
    not translate to exciting seminars.</p>

    <p>The selected Highlights will be presented in a special track
    during the RuleML-2008 Symposium. All presentations will have to
    be completed within 20 minutes and will be followed by 5-minute
    discussions. While presenters are expected to focus mostly on the
    chosen paper(s), short infusions of more recent data are
    welcome.</p>

    <p>Decisions will be notified by September 1st.</p>

    <hr style="margin-top: 40px;">

    <a name="fast"><h3>Fast Abstracts</h3></a>

    <p>Fast Abstracts at RuleML-2008 are short presentations, either
    on new ideas or work in progress, or radical opinions that can
    address any issue relevant to RuleML-2008. Fast Abstracts provide
    an opportunity to receive early feedback from the
    community. Contributions are particularly solicited from
    industrial practitioners and academics that may not have been able
    to prepare full papers, but seek an opportunity to engage with the
    RuleML community.</p>

    <p>Fast Abstracts should be 4-pages long, and must be formatted in
    <a href="http://www.springer.de/comp/lncs/authors.html">LNCS
    format</a>.  The submission deadline is August 13, 2008.
    Submissions should be sent directly by email to
    ruleml2008@easychair.org, and they will be refereed on the
    relevance to RuleML 2008, but also on their novelty of idea and/or
    on their capacity to stimulate and intrigue the reader. Accepted
    contributions will be published in electronic form (at the
    Symposium's Web site and on CD), and an author will deliver a
    short talk in the Fast Abstracts track at the
    conference. Decisions will be notified by September 1st. Authors
    of accepted fast abstracts must provide the camera ready version
    by September 15. At least one author of each accepted Fast
    Abstract is expected to register to the conference before or on
    September 15.</p>

 
