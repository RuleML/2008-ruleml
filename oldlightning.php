    <a name="lightning"><h3>Lightning Talks</h3></a>

    <p>A lightning talk is a five-minute presentation on any topic of
    interest to the RuleML community; it can be a new idea, a
    technology, an evaluation, an observation, a complaint, an
    explanation, a suggestion, a report of success or failure, a call
    to action, a description of a technique, or a lament. In general,
    it is supposed to be a short visionary talk which should initiate
    discussion. If you are a rule developer working on an exciting
    project and you do not have the time to submit a full paper,
    Lightning talks are a great way to interact with the RuleML
    community and receive feedback on your ideas. Lightning talks are
    presented back-to-back with a strictly enforced five minute limit,
    so make sure that you can fit your presentation within this time
    span.</p>

    <p>People interested at giving a lightning talk during RuleML-2008
    should show their their interest by sending an email to <a
    href="mailto:ruleml2008@easychair.org">ruleml2008@easychair.org</a>
    by August 13, 2008, including a title and a 250-word abstract of
    their intended talk.</p>

    <p>Decisions will be notified by September 1st.</p>

