    <a name="soft"><h3>RuleML-2008 Challenge - 'Soft' Demos</h3></a>

    <p>The RuleML-2008 Challenge addresses system demonstrations
    showcasing the practical use of rule technologies in distributed
    and/or Web-based environments. The focus of the challenge is on
    rule technologies (including rule languages and engines),
    interoperation, and interchange. The challenge offers participants
    the chance to demonstrate their commercial and open source tools,
    use cases, and applications. Prizes will be awarded to the two
    best applications. All accepted demos will be presented in a
    special Challenge Session.</p>

    <p>A submission to the RuleML-2008 Challenge has to meet the
    requirement that explicit (declarative) rules play a central role
    in the application. For the 2008 Challenge this means that:</p>

    <ul>

      <li>Rules are available in the explicit representation of a
      declarative format and are decoupled from the application
      (rather than only existing in a form that is compiled or
      hard-coded into the application logic).</li>

      <li>Rules are used in interesting and practically relevant ways,
      e.g. to derive new information, transform knowledge, provide
      decision support; or, to enable automated monitoring,
      enforcement, validation, or management of the logic of an
      application.</li>

    </ul>

    <p>The demo should preferably (but not necessarily) be embedded
    into a web-based or distributed environment so that there will be
    a need for features related to the <a href="topics.php">RuleML
    symposium topics</a>. Demos will be evaluated during RuleML-2008,
    and prizes will be awarded to the first two best applications.</p>

    <p>Submissions to the RuleML Challenge 2008 consist of (1) a link
    to information about the demo/showcase, e.g. a project site, an
    (embedded link to an) online demonstration, a presentation, or a
    download site and (2) a demo paper of up to 3 pages (e.g., in LNCS
    format), describing the demo showcase. Demo papers will be subject
    to a review process, so a demo paper should contain a substantial
    presentation of the system to enable a proper evaluation of the
    techniques used. The demo link should be submitted through the <a
    href="http://ruleml-challenge.cs.nccu.edu.tw/">Challenge
    Website</a>, while the demo paper should be submitted as a pdf
    file by email to: <a
    href="mailto:ruleml2008@easychair.org">ruleml2008@easychair.org</a>
    with the text 'Submission to RuleML-2008 Challenge' in the
    subject. Submitted demo links will be immediately available on the
    Web, which can provide instant visibility.</p>


