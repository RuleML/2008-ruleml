    <h2>Overview</h2>

    <p>The International RuleML Symposium on Rule Interchange and
    Applications (<a href="http://2008.ruleml.org">RuleML-2008</a>) is
    devoted to practical distributed rule technologies and rule-based
    applications which need language standards for rules operating in
    the context of, e.g., the Semantic Web, Intelligent Multi-Agent
    Systems, Event-Driven Architectures and Service-Oriented Computing
    Applications.</p>


